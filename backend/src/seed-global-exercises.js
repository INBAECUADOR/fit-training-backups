async function seedGlobalExercises() {
  const db = await getDb();
  const existing = db.exec(`SELECT COUNT(*) FROM global_exercises`);
  const count = existing.length > 0 ? existing[0].values[0][0] : 0;

  if (count > 0) {
    console.log(`Catálogo global ya tiene ${count} ejercicios`);
    return;
  }

  let inserted = 0;
  for (const [group, exercises] of Object.entries(exercisesByGroup)) {
    for (const name of exercises) {
      db.run(`INSERT INTO global_exercises (name, muscle_group) VALUES (?, ?)`, [name, group]);
      inserted++;
    }
  }

  saveDb();
  console.log(`Catálogo global seedeado con ${inserted} ejercicios en ${Object.keys(exercisesByGroup).length} grupos musculares`);
}

seedGlobalExercises().catch(console.error);
